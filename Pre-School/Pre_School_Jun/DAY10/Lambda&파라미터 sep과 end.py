# Lambda(람다)
# 이름 없는 한 줄 함수 즉, 익명 함수
# 변수명 = lambda 인수1, 인수2, ... : 실행코드 ( 리턴 값 )


# ------------------------------------------------------------
print(10,20,30)
print(10,20,30,sep='*')
print(10,20,30,end='')
print(10,20,30,40,end='ㅋㅋ')
print(10,20,30)
print(10,20,30)
print(10,20,30,)

# 파라미터 sep은 디폴트 값이 공백
# 파라미터 end는 디폴트 값이 줄 바꿈. \n
