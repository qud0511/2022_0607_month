# 컴퓨터 메모리에 문자열 데이터 저장
msg='Hello'

# 1번요소 읽어와서 출력하기
print(msg[1])

# 1번요소의 값을 e -> E로 변경
# msg[1]='E'는 틀린 표현. str 타입은 요소값 변경 불가.

msg2=msg[0]+'E'+msg[2:]
#    'H'   +'E'+'llo'
print(msg, msg2)

