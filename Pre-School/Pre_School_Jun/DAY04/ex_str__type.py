# 생년월일을 문자열 포맷팅으로 만들기
year=2022
month=3
day=12
birth="나의 생일은 %s년 %s월 %d일입니다." %(year, month, day)
print(birth)

# 문자열 코드
# %s => 문자열 string -> 파이썬에서는 어떤 형태의 값이든 문자열로 취급해서 변환해줌.
# %c => 문자 1개 character
# %d => 정수 integer
# %f => 부동 소수 floating-point
# %o => 8진수 octal
# %x => 16진수 Hexadecimal
# %% => literal % 문자 '%' 자체
test='%s%%' %30
print(test)

# 문자열 포맷팅의 정렬과 공백
# '____hi____'
# 'hi________'
# '________hi'
print("%10s" %'Good')
print("%-10s" %'Good')
print("%25s" %'Merry Chirstmas')
print("%-10s" %'Merry Chirstmas')

# 교재 61쪽
print("%-10sJane" %'Hi')

# 교재 61쪽, 소수점 표현하기
print("%0.4f" % 3.42134234)
print("%10.4f" % 3.42134234)

